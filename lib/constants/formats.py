# ============================================================================
# 音频格式
# ============================================================================

# 支持质量探测的音频格式和排序格式
ALLOWED_READ_AUDIO_FORMAT = [
    ".dsf", ".wv", ".wav", ".alfc", ".aif", ".aiff",
    ".flac", ".m4a", ".mp3", ".ogg", ".wma"
]

# 音频格式优先级映射（用于排序）
AUDIO_FORMAT_ORDER = {ext: i for i, ext in enumerate(ALLOWED_READ_AUDIO_FORMAT)}

# 可以直接解码为PCM进行分割的格式（无需经过转码流程）
DIRECT_SPLIT_FORMATS = {'.wav', '.wv', '.aiff', '.aif', '.m4a', '.tak', '.tta', '.ape', ".flac"}

# ============================================================================
# 图片格式
# ============================================================================

# 支持的图片格式（用于 booklet 检测）
IMAGE_FORMATS = {".jpg", ".jpeg", ".png", ".webp", ".jxl"}
